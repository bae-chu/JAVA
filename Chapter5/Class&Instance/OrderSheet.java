package classpart;

public class OrderSheet {
	
	long odnum;
	String id;
	String oddate;
	String name;
	String pdnum;
	String address;
	
	public long getOdnum() {
		return odnum;
	}
	public void setOdnum(long odnum) {
		this.odnum = odnum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOddate() {
		return oddate;
	}
	public void setOddate(String oddate) {
		this.oddate = oddate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPdnum() {
		return pdnum;
	}
	public void setPdnum(String pdnum) {
		this.pdnum = pdnum;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
}
